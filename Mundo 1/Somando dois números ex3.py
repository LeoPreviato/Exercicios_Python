# Programa para somar dois números
# Este programa solicita que o usuário digite dois números e exibe a soma deles

# Coleta o primeiro número do usuário e converte para inteiro
num1 = int(input("Digite o primeiro número: "))

# Coleta o segundo número do usuário e converte para inteiro
num2 = int(input("Digite o segundo número: "))

# Exibe o resultado da soma usando f-string para formatar a mensagem
print(f"A soma entre {num1} + {num2} é igual a {num1 + num2}")
